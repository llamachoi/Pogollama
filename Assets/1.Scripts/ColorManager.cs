using UnityEngine;

public class ColorManager : MonoBehaviour
{
    public Color[] colors;

    public static ColorManager Instance { get; private set; }

    private void Awake()
    {
        if (Instance != null && Instance != this)
        {
            Destroy(gameObject);
        }
        else
        {
            Instance = this;
            DontDestroyOnLoad(gameObject);
        }
    }
}
